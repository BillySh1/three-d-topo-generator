function treeLayout(root, isRoot) {
  const children = root.children;
  const degree = root.degree;
  const r = root.radius;
  if (isRoot) {
    root.positionZ = 0;
  }
  root.positionX = -5000 + root.depth * 3000;
  root.positionY = 0;
  children.forEach((child, index) => {
    const s = (index % 2 === 0) ? -degree * index : degree * index
    const x = s * r;

    child.positionZ = root.positionZ + x;
    treeLayout(child)
  })


}

function countRadius(root, minR) {
  minR = (minR == null ? 150 : minR);

  // 若果是末端节点，则设置其布局半径及领域半径为最小半径
  if (!root.children.length) {
    root.radius = minR
    root.totalRadius = minR
    return;
  }
  const children = root.children
  // 遍历孩子节点递归计算半径
  children.forEach((child) => {
    countRadius(child, minR);
  });

  const child0 = root.children[0];
  // 获取孩子节点半径
  const radius = child0.radius;
  const totalRadius = child0.totalRadius;

  // 计算子节点的1/2张角
  const degree = Math.PI / children.length;
  // 计算父亲节点的布局半径
  const pRadius = totalRadius / Math.sin(degree);

  // 缓存父亲节点的布局半径
  root.radius = pRadius
  // 缓存父亲节点的领域半径
  root.totalRadius = pRadius + totalRadius
  // 缓存其孩子节点的布局张角
  root.degree = degree * 2

}

