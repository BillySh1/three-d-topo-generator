function genTree(node, link, rootId, tree) {
  if (
    !node.filter((nodeItem) => {
      return nodeItem.id === rootId;
    })
  ) {
    return;
  }
  if (!tree.id) {
    tree.id = rootId;
    tree.pid = rootId
    tree.checked = true;
    tree.expand = true;
  }else{
    tree.expand = false;
  }
  tree.children = [];
  let index = node.findIndex(item => {
    return item.id === tree.id
  });
  if (index !== -1) {
    tree.name = node[index].name.split('-')[0];
  }
  for (let i = 0; i < link.length; i++) {
    if (link[i].source === rootId) {
      tree.children.push({ id: link[i].target, pid: link[i].source });
      link.splice(i, 1);
      i--;
    }
  }
  if (tree.children.length === 0) {
    return;
  } else {
    tree.children.forEach((child) => {
      genTree(node, link, child.id, child);
    });
  }
}

const treePaths = []
function iterTree(tree, index, uNodes, path = []) {
  if (!tree.depthSetted) {
    tree.depth = index;
    tree.depthSetted = true;
  }
  index++
  if (!tree.settedPos) {
    path.push(tree.id);
    tree.settedPos = true
  }

  uNodes.forEach((node) => {
    if (node.id === tree.id) {
      if (!node.settedDepth) {
        node.depth = tree.depth;
        node.settedDepth = true;
      }
    }
  })
  const parentIdx = index;
  if (tree.children.length > 0) {
    tree.children.forEach((child) => {
      iterTree(child, parentIdx, uNodes, [...path])
    })
  } else {
    treePaths.push(path)
  }
}

function getPosZ(idx1, idx2, depth) {
  let pos = 0
  if (idx2 % 2 === 0) {
    pos = -(idx1 * 200 - idx2 * 400)
  } else {
    pos = (idx1 * 200 - idx2 * 400)
  }
  if (idx1 === 0) {
    return 0
  }
  if (depth === 1) {
    if (idx1 % 2 === 0) {
      return idx1 * 200
    } else {
      return -idx1 * 200
    }
  }
  return pos
}

function getPosX(depth, id, uNodes) {
  let index = uNodes.findIndex(node => {
    return node.id === id
  })
  if (depth === 1) {
    if (index % 2 === 0) {
      return -2500
    } else {
      return -3500
    }
  }
  return -6000 + depth * 3000
}

function formatDepth1Pos(nodes) {
  const dp1Arr = []
  for (let i = 0; i < nodes.length; i++) {
    const node = nodes[i];
    if (node.depth === 1) {
      dp1Arr.push(node)
    }
  }
  dp1Arr.sort((a, b) => {
    return b.positionZ - a.positionZ
  })
  dp1Arr.forEach((node, index) => {
    node.positionZ = 7200 - 800 * index;
    node.positionX = index % 2 === 0 ? -2500 : -3500;
  })
}
/**
 * @params
 * i:第几条路径
 * index：节点在路径中的索引
*/
function posNodes(uNodes) {
  treePaths.sort((a, b) => {
    return b.length - a.length
  })
  for (let i = 0; i < treePaths.length; i++) {
    uNodes.forEach((node) => {
      let index = treePaths[i].findIndex((item) => {
        return item === node.id
      })
      if (!node.settedPos) {
        if (index !== -1) {
          node.positionX = getPosX(node.depth, node.id, uNodes);
          node.positionY = 0;
          node.positionZ = getPosZ(i, index, node.depth);
          node.settedPos = true;
        }
      }
    })
  }
}


